// ---- global-insights.md -----------------------------------------------------

async function seedGlobalInsights(): Promise<void> {
  const filePath = join(MEMORY_ROOT, "global-insights.md");

  const allSnapshots = await prisma.cachedMetrics.findMany({
    where: { metricType: "campaign_snapshot" },
    select: { workspace: true, data: true, metricKey: true },
  });

  type ParsedSnapshot = {
    workspace: string;
    replyRate?: number;
    openRate?: number;
    bounceRate?: number;
    totalLeadsContacted?: number;
    channels?: string[];
    status?: string;
  };

  const parsed: ParsedSnapshot[] = [];
  for (const snapshot of allSnapshots) {
    try {
      const d = JSON.parse(snapshot.data as string);
      const total = d.totalLeadsContacted ?? 0;
      const status = d.status ?? "";
      if (total >= 50 && (status === "active" || status === "completed" || status === "paused")) {
        parsed.push({
          workspace: snapshot.workspace,
          replyRate: d.replyRate,
          openRate: d.openRate,
          bounceRate: d.bounceRate,
          totalLeadsContacted: total,
          channels: Array.isArray(d.channels) ? d.channels : ["email"],
          status,
        });
      }
    } catch {
      // skip
    }
  }

  // Compute cross-client email averages
  const emailCampaigns = parsed.filter((p) => !p.channels || p.channels.includes("email"));
  const avgReplyRate = emailCampaigns.length > 0
    ? emailCampaigns.reduce((s, p) => s + (p.replyRate ?? 0), 0) / emailCampaigns.length
    : null;
  const avgOpenRate = emailCampaigns.length > 0
    ? emailCampaigns.reduce((s, p) => s + (p.openRate ?? 0), 0) / emailCampaigns.length
    : null;
  const avgBounceRate = emailCampaigns.length > 0
    ? emailCampaigns.reduce((s, p) => s + (p.bounceRate ?? 0), 0) / emailCampaigns.length
    : null;

  // Group by workspace vertical
  const workspaces = await prisma.workspace.findMany({
    select: { slug: true, vertical: true },
  });
  const verticalMap: Record<string, string> = {};
  for (const ws of workspaces) {
    if (ws.vertical) verticalMap[ws.slug] = ws.vertical;
  }

  // Vertical averages
  const byVertical: Record<string, { count: number; replySum: number; openSum: number }> = {};
  for (const p of emailCampaigns) {
    const vertical = verticalMap[p.workspace] ?? "Unknown";
    if (!byVertical[vertical]) byVertical[vertical] = { count: 0, replySum: 0, openSum: 0 };
    byVertical[vertical].count++;
    byVertical[vertical].replySum += p.replyRate ?? 0;
    byVertical[vertical].openSum += p.openRate ?? 0;
  }

  const verticalRows = Object.entries(byVertical)
    .sort((a, b) => b[1].count - a[1].count)
    .map(([vertical, v]) => {
      const avgReply = ((v.replySum / v.count) * 100).toFixed(1);
      const avgOpen = ((v.openSum / v.count) * 100).toFixed(1);
      return `| ${vertical} | ${v.count} | ${avgReply}% | ${avgOpen}% |`;
    });

  const benchmarksSection = parsed.length > 0
    ? `## Cross-Client Benchmarks

_Based on ${emailCampaigns.length} email campaigns with 50+ leads contacted, across all active workspaces._

| Metric | Value |
|--------|-------|
| Avg Reply Rate (email) | ${avgReplyRate != null ? (avgReplyRate * 100).toFixed(1) + "%" : "No data"} |
| Avg Open Rate (email) | ${avgOpenRate != null ? (avgOpenRate * 100).toFixed(1) + "%" : "No data"} |
| Avg Bounce Rate (email) | ${avgBounceRate != null ? (avgBounceRate * 100).toFixed(1) + "%" : "No data"} |

### By Vertical

| Vertical | Campaigns | Avg Reply Rate | Avg Open Rate |
|----------|-----------|----------------|---------------|
${verticalRows.join("\n") || "| — | — | — | — |"}`
    : `## Cross-Client Benchmarks

_No campaign data with 50+ leads yet. Run campaigns and re-seed to populate benchmarks._`;

  const content = `<!-- global-insights.md | seeded: ${isoDate()} | writer: nova-intel ONLY -->
<!-- Write governance: ONLY nova-intel agent may write here. All other agents: READ ONLY. APPEND with timestamp. Max 200 lines. -->

# Global Insights — Cross-Client Intelligence

${benchmarksSection}

## Best Performing Channels

<!-- nova-intel: append findings as: [ISO date] — [channel] outperforms [other channel] for [vertical] by [X%] reply rate -->

(Populated by nova-intel after campaign data accumulates)

## Copy Patterns

<!-- nova-intel: append patterns as: [ISO date] — [pattern description, e.g. "One-liner strategy outperforms PVP for recruitment vertical by 1.2x"] -->

(Populated by nova-intel after campaign data accumulates)

## Operational Insights

<!-- nova-intel: append insights as: [ISO date] — [operational pattern, e.g. "Tuesday/Wednesday sends get 15% higher open rates", "AI Ark leads show 10% higher reply rate than Apollo for SaaS"] -->

(Populated by nova-intel after campaign data accumulates)
`;

  await writeFile(filePath, content, "utf8");
}

// ---- Per-workspace seed -----------------------------------------------------

async function seedWorkspace(slug: string): Promise<void> {
  const dirPath = join(MEMORY_ROOT, slug);

  await mkdir(dirPath, { recursive: true });
  // Create .gitkeep in slug directory
  await writeFile(join(dirPath, ".gitkeep"), "", "utf8");

  // profile.md — always overwrite
  await writeProfile(slug, dirPath);

  // campaigns.md — skip if exists
  const campaignsResult = await writeCampaigns(slug, dirPath);

  // feedback.md — skip if exists
  const feedbackResult = await writeFeedback(slug, dirPath);

  // learnings.md — skip if exists
  const learningsResult = await writeLearnings(slug, dirPath);

  const profileStatus = "overwritten";
  const campaignsStatus = campaignsResult.created ? "created" : "skipped";
  const feedbackStatus = feedbackResult.created ? "created" : "skipped";
  const learningsStatus = learningsResult.created ? "created" : "skipped";

  console.log(
    `  Seeding ${slug}... profile.md (${profileStatus}), campaigns.md (${campaignsStatus}), feedback.md (${feedbackStatus}), learnings.md (${learningsStatus})`
  );
}

// ---- Main -------------------------------------------------------------------

async function main() {
  const args = process.argv.slice(2);
  const command = args[0];

  if (!command || command === "--help" || command === "-h") {
    console.log(`
Nova Memory CLI — Seed per-workspace memory directories for Nova CLI skills.

Usage:
  npx tsx scripts/nova-memory.ts seed {slug}    Seed one workspace
  npx tsx scripts/nova-memory.ts seed --all     Seed all workspaces from DB

Examples:
  npx tsx scripts/nova-memory.ts seed rise
  npx tsx scripts/nova-memory.ts seed --all
`);
    process.exit(0);
  }

  if (command !== "seed") {
    console.error(`Unknown command: '${command}'. Run with --help for usage.`);
    process.exit(1);
  }

  const target = args[1];
  if (!target) {
    console.error("Usage: nova-memory.ts seed {slug|--all}");
    process.exit(1);
  }

  // Ensure memory root + root .gitkeep exist
  await mkdir(MEMORY_ROOT, { recursive: true });
  const rootGitkeep = join(MEMORY_ROOT, ".gitkeep");
  if (!(await fileExists(rootGitkeep))) {
    await writeFile(rootGitkeep, "", "utf8");
  }

  if (target === "--all") {
    const workspaces = await prisma.workspace.findMany({
      select: { slug: true },
      orderBy: { slug: "asc" },
    });

    console.log(`Seeding ${workspaces.length} workspaces...\n`);
    for (const ws of workspaces) {
      await seedWorkspace(ws.slug);
    }

    console.log("\nSeeding global insights...");
    await seedGlobalInsights();
    console.log("  global-insights.md (overwritten)");

    console.log(`\nDone. Memory seeded at ${MEMORY_ROOT}/`);
  } else {
    // Single workspace
    await seedWorkspace(target);

    console.log("\nSeeding global insights...");
    await seedGlobalInsights();
    console.log("  global-insights.md (overwritten)");

    console.log(`\nDone. Memory seeded at ${MEMORY_ROOT}/${target}/`);
  }

  await prisma.$disconnect();
}

main().catch((err) => {
  console.error("Error:", err.message);
  prisma.$disconnect();
  process.exit(1);
});
