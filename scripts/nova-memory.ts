/**
 * Nova Memory CLI — Seed per-workspace memory directories for Nova CLI skills.
 *
 * Usage:
 *   npx tsx scripts/nova-memory.ts seed {slug}    — seed one workspace
 *   npx tsx scripts/nova-memory.ts seed --all     — seed all workspaces from DB
 *
 * Memory files created per workspace:
 *   .nova/memory/{slug}/profile.md     — always overwritten (fresh from DB)
 *   .nova/memory/{slug}/campaigns.md   — skipped if exists (append-only)
 *   .nova/memory/{slug}/feedback.md    — skipped if exists (append-only)
 *   .nova/memory/{slug}/learnings.md   — skipped if exists (append-only)
 *
 * Global:
 *   .nova/memory/global-insights.md    — always overwritten (cross-client averages)
 */

import { config } from "dotenv";
config({ path: ".env.local" });

import { mkdir, writeFile, access } from "fs/promises";
import { constants } from "fs";
import { join } from "path";
import { PrismaClient } from "@prisma/client";

const prisma = new PrismaClient();
const MEMORY_ROOT = ".nova/memory";

// ---- Helpers ----------------------------------------------------------------

async function fileExists(filePath: string): Promise<boolean> {
  try {
    await access(filePath, constants.F_OK);
    return true;
  } catch {
    return false;
  }
}

function isoDate(): string {
  return new Date().toISOString().split("T")[0];
}

function or(value: string | null | undefined, fallback: string): string {
  if (!value || value.trim() === "") return fallback;
  return value.trim();
}

function parseJsonArray(raw: string | null | undefined): string[] {
  if (!raw) return [];
  try {
    const parsed = JSON.parse(raw);
    return Array.isArray(parsed) ? parsed : [];
  } catch {
    return [];
  }
}

// ---- profile.md -------------------------------------------------------------

async function writeProfile(slug: string, dirPath: string): Promise<void> {
  const workspace = await prisma.workspace.findUnique({
    where: { slug },
    select: {
      slug: true,
      name: true,
      vertical: true,
      website: true,
      package: true,
      icpCriteriaPrompt: true,
      icpCountries: true,
      icpIndustries: true,
      icpCompanySize: true,
      icpDecisionMakerTitles: true,
      coreOffers: true,
      painPoints: true,
      differentiators: true,
      normalizationPrompt: true,
      outreachTonePrompt: true,
      enabledModules: true,
      monthlyCampaignAllowance: true,
      monthlyLeadQuota: true,
      senderFullName: true,
      senderJobTitle: true,
      onboardingNotes: true,
    },
  });

  if (!workspace) {
    throw new Error(`Workspace '${slug}' not found in database`);
  }

  const enabledModules = parseJsonArray(workspace.enabledModules);
  const channels = enabledModules.length > 0 ? enabledModules.join(", ") : "email";

  const icpSection = [
    workspace.icpCriteriaPrompt
      ? `**ICP Description:** ${workspace.icpCriteriaPrompt}`
      : `**ICP Description:** Not configured`,
    workspace.icpIndustries
      ? `**Industries:** ${workspace.icpIndustries}`
      : `**Industries:** Not configured`,
    workspace.icpCountries
      ? `**Countries:** ${workspace.icpCountries}`
      : `**Countries:** Not configured`,
    workspace.icpCompanySize
      ? `**Company Size:** ${workspace.icpCompanySize}`
      : `**Company Size:** Not configured`,
    workspace.icpDecisionMakerTitles
      ? `**Decision Maker Titles:** ${workspace.icpDecisionMakerTitles}`
      : `**Decision Maker Titles:** Not configured`,
  ].join("\n");

  const content = `<!-- profile.md | workspace: ${slug} | seeded: ${isoDate()} | re-seed: overwrites this file -->
<!-- Write governance: NEVER append to this file — it is regenerated by nova-memory seed -->

# ${workspace.name} — Workspace Profile

## Company

| Field | Value |
|-------|-------|
| Name | ${or(workspace.name, "Not set")} |
| Vertical | ${or(workspace.vertical, "Not configured")} |
| Website | ${or(workspace.website, "Not configured")} |
| Package | ${workspace.package} |
| Enabled Channels | ${channels} |
| Monthly Campaign Allowance | ${workspace.monthlyCampaignAllowance} |
| Monthly Lead Quota | ${workspace.monthlyLeadQuota} |

## ICP

${icpSection}

${workspace.coreOffers ? `**Core Offers:**\n${workspace.coreOffers}` : "**Core Offers:** Not configured"}

${workspace.painPoints ? `**Pain Points:**\n${workspace.painPoints}` : "**Pain Points:** Not configured"}

${workspace.differentiators ? `**Differentiators:**\n${workspace.differentiators}` : "**Differentiators:** Not configured"}

## Tone & Voice

${workspace.outreachTonePrompt ? workspace.outreachTonePrompt : "Not configured — use professional but friendly tone by default"}

${workspace.normalizationPrompt ? `**Normalization Rules:**\n${workspace.normalizationPrompt}` : ""}

## Outreach Channels

Active: ${channels}

## Key Contacts

| Field | Value |
|-------|-------|
| Sender Name | ${or(workspace.senderFullName, "Not configured")} |
| Sender Title | ${or(workspace.senderJobTitle, "Not configured")} |

## Onboarding Notes

${or(workspace.onboardingNotes, "None recorded")}

## Standing Instructions

<!-- Admin-editable section. Examples: "Rise prefers short sequences", "Never use hyphens in copy", "Covenco wants formal tone only" -->
<!-- Agents: read this section before every task. These instructions override defaults. -->

(No standing instructions configured yet — admin can edit this file directly to add client-specific rules)
`;

  await writeFile(join(dirPath, "profile.md"), content, "utf8");
}

// ---- campaigns.md -----------------------------------------------------------

async function writeCampaigns(slug: string, dirPath: string): Promise<{ created: boolean }> {
  const filePath = join(dirPath, "campaigns.md");
  if (await fileExists(filePath)) {
    return { created: false };
  }

  const campaigns = await prisma.campaign.findMany({
    where: { workspaceSlug: slug },
    orderBy: { createdAt: "desc" },
    select: {
      id: true,
      name: true,
      channels: true,
      status: true,
      createdAt: true,
    },
  });

  const campaignIds = campaigns.map((c) => c.id);

  let metricsMap: Record<string, { replyRate?: number; openRate?: number; bounceRate?: number; totalLeadsContacted?: number }> = {};

  if (campaignIds.length > 0) {
    const snapshots = await prisma.cachedMetrics.findMany({
      where: {
        metricType: "campaign_snapshot",
        metricKey: { in: campaignIds },
      },
      select: {
        metricKey: true,
        data: true,
      },
    });

    for (const snapshot of snapshots) {
      try {
        const parsed = JSON.parse(snapshot.data as string);
        metricsMap[snapshot.metricKey] = {
          replyRate: parsed.replyRate,
          openRate: parsed.openRate,
          bounceRate: parsed.bounceRate,
          totalLeadsContacted: parsed.totalLeadsContacted,
        };
      } catch {
        // skip malformed snapshot
      }
    }
  }

  const rows = campaigns.map((c) => {
    const channels = parseJsonArray(c.channels).join(", ") || "email";
    const m = metricsMap[c.id] || {};
    const replyRate = m.replyRate != null ? `${(m.replyRate * 100).toFixed(1)}%` : "—";
    const openRate = m.openRate != null ? `${(m.openRate * 100).toFixed(1)}%` : "—";
    const leads = m.totalLeadsContacted != null ? String(m.totalLeadsContacted) : "—";
    return `| ${c.name} | ${channels} | ${c.status} | ${replyRate} | ${openRate} | ${leads} |`;
  });

  const tableSection = campaigns.length > 0
    ? `| Campaign | Channel | Status | Reply Rate | Open Rate | Leads |
|----------|---------|--------|------------|-----------|-------|
${rows.join("\n")}`
    : "_No campaigns recorded yet._";

  const content = `<!-- campaigns.md | workspace: ${slug} | seeded: ${isoDate()} | re-seed: skips if exists -->
<!-- Write governance: APPEND only, with ISO timestamp. Max 200 lines. Never delete existing entries. -->

# ${slug} — Campaign History

## Campaign Performance

${tableSection}

## Copy Wins

<!-- Agent: append entries as: [ISO date] Campaign: [name] — [what worked and why] -->

(No copy wins recorded yet)

## Copy Losses

<!-- Agent: append entries as: [ISO date] Campaign: [name] — [what failed and why] -->

(No copy losses recorded yet)
`;

  await writeFile(filePath, content, "utf8");
  return { created: true };
}

