const { PrismaClient } = require('@prisma/client');
const prisma = new PrismaClient();

async function main() {
  // 1. Check/create InvoiceSenderSettings
  const existing = await prisma.invoiceSenderSettings.findFirst();
  if (existing) {
    console.log('InvoiceSenderSettings already exists:', existing.id);
  } else {
    const settings = await prisma.invoiceSenderSettings.create({
      data: {
        senderName: 'Jonathan Melhuish-Sprague',
        senderAddress: '22 Mulligan Drive, Exeter, EX2 7SJ',
        senderEmail: 'jonathan@melhuishsprague.com',
        accountNumber: '20963400',
        sortCode: '60-83-71',
      },
    });
    console.log('Created InvoiceSenderSettings:', settings.id);
  }

  // 2. Check existing invoices for lime-recruitment
  const existingInvoices = await prisma.invoice.findMany({
    where: { workspaceSlug: 'lime-recruitment' },
    select: { invoiceNumber: true },
  });
  if (existingInvoices.length > 0) {
    console.log('Invoices already exist for lime-recruitment:', existingInvoices.map(i => i.invoiceNumber));
    console.log('Skipping creation.');
    return;
  }

  // Shared data
  const sender = {
    senderName: 'Jonathan Melhuish-Sprague',
    senderAddress: '22 Mulligan Drive, Exeter, EX2 7SJ',
    senderEmail: 'jonathan@melhuishsprague.com',
    bankDetails: 'Account: 20963400, Sort Code: 60-83-71',
  };
  const client = {
    clientCompanyName: 'Paytier Solutions Ltd',
    clientAddress: 'Arkwright House, Parsonage Gardens, Manchester, M3 2LF',
  };
  const lineItems = [
    { description: 'Cold Outbound Retainer', quantity: 1, unitPricePence: 75000, amountPence: 75000 },
    { description: 'Cold Outbound Platform Fee', quantity: 1, unitPricePence: 38000, amountPence: 38000 },
  ];

  const invoices = [
    {
      invoiceNumber: 'PS01',
      issueDate: new Date('2025-11-25'),
      dueDate: new Date('2025-11-26'),
      billingPeriodStart: new Date('2025-11-26'),
      billingPeriodEnd: new Date('2025-12-24'),
      paidAt: new Date('2025-11-26'),
    },
    {
      invoiceNumber: 'PS02',
      issueDate: new Date('2025-12-29'),
      dueDate: new Date('2025-12-30'),
      billingPeriodStart: new Date('2025-12-29'),
      billingPeriodEnd: new Date('2026-01-26'),
      paidAt: new Date('2025-12-30'),
    },
    {
      invoiceNumber: 'PS03',
      issueDate: new Date('2026-01-26'),
      dueDate: new Date('2026-01-28'),
      billingPeriodStart: new Date('2026-01-26'),
      billingPeriodEnd: new Date('2026-02-23'),
      paidAt: new Date('2026-01-28'),
    },
  ];

  // 3. Create invoices
  for (const inv of invoices) {
    const created = await prisma.invoice.create({
      data: {
        invoiceNumber: inv.invoiceNumber,
        workspaceSlug: 'lime-recruitment',
        ...sender,
        ...client,
        issueDate: inv.issueDate,
        dueDate: inv.dueDate,
        billingPeriodStart: inv.billingPeriodStart,
        billingPeriodEnd: inv.billingPeriodEnd,
        subtotalPence: 113000,
        taxRate: 0,
        taxAmountPence: 0,
        totalPence: 113000,
        status: 'paid',
        paidAt: inv.paidAt,
        autoGenerated: false,
        lineItems: {
          create: lineItems,
        },
      },
      include: { lineItems: true },
    });
    console.log(`Created invoice ${created.invoiceNumber} (id: ${created.id}) with ${created.lineItems.length} line items`);
  }

  // 4. Upsert InvoiceSequence
  const seq = await prisma.invoiceSequence.upsert({
    where: { workspaceSlug: 'lime-recruitment' },
    update: { lastNumber: 3 },
    create: { workspaceSlug: 'lime-recruitment', lastNumber: 3 },
  });
  console.log(`InvoiceSequence for lime-recruitment set to lastNumber: ${seq.lastNumber}`);

  console.log('\nDone!');
}

main()
  .catch((e) => { console.error(e); process.exit(1); })
  .finally(() => prisma.$disconnect());
